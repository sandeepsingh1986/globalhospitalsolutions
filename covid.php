<p>
	
<div class="row" style="color:red; font-weight:bold;"><h1>Home treatment - Instructions for Caregivers</h1></div>  		

<div class="row">Mask: The caregiver should wear a triple layer medical mask appropriately when in the same room with the ill person. Front portion of the mask should not be touched or handled during use. If the mask gets wet or dirty with secretions, it must be changed immediately. Discard the mask after use and perform hand hygiene after disposal of the mask.</div>	
	
<p>
	
<div class="row">Hand hygiene must be ensured following contact with ill person or his immediate environment. Hand hygiene should also be practiced before and after preparing food, before eating, after using the toilet, and whenever hands look dirty. Use soap and water for hand washing at least for 40 seconds. Alcohol-based hand rub can be used, if hands are not visibly soiled. After using soap and water, use of disposable paper towels to dry hands is desirable. If not available, use dedicated clean cloth towels and replace them when they become wet.</div>	

<p>
	
<div class="row">Exposure to patient: Avoid direct contact with body fluids of the patient, particularly oral or respiratory secretions. Use disposable gloves while handling the patient. Perform hand hygiene before and after removing gloves.</div>	

<p>
	
<div class="row">Avoid exposure to potentially contaminated items in his immediate environment (e.g. avoid sharing cigarettes, eating utensils, dishes, drinks, used towels or bed linen).</div>	

<p>
	
<div class="row">Food must be provided to the patient in his room.</div>

<p>
	
<div class="row">Utensils and dishes used by the patient should be cleaned with soap/detergent and water wearing gloves. The utensils and dishes may be re-used. Clean hands after taking off gloves or handling used items.</div>

<p>
	
<div class="row">Use triple layer medical mask and disposable gloves while cleaning or handling surfaces, clothing or linen used by the patient. Perform hand hygiene before and after removing gloves. The care giver will make sure that the patient follows the prescribed treatment.
</div>

<p>	
	
<div class="row">The care giver and all close contact should self-monitor their health with daily temperature monitoring and report promptly if they develop any symptom suggestive of COVID-19 (fever/cough/difficulty in breathing/ loss of smell and taste).</div>

<p>
	
<div class="row">If care giver suffers with any of these symptoms, he/ she should consult to physician immediately who will guide you for COVID testing and treatment if required.</div>


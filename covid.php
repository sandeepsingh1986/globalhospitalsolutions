<div class="row" style="color:red; font-weight:bold;"><h1>COVID-19: How it spreads</h1></div>  		

<div class="row">COVID-19 is an infectious disease caused by the recently discovered coronavirus. The COVID-19 virus spreads primarily through droplets of saliva or discharge from the nose when an infected person coughs or sneezes. The virus enters the body via nose, mouth and eyes. Some recent studies suggest that virus may be airborne and can be spread through fine infected droplets that remain suspended in the air in closed air-conditioned environments of offices, AC cabs-buses, shopping malls and theatres due to absence of cross-ventilation, even when you are not in direct contact with an infected person.</div>	

	
<p><p><p><p>	
<div class="row">COVID-19 transmission can be reduced by:</div>	 
	
<div class="row"><ul></div>
<div class="row"><li>Staying at home if possible, especially if you are more than 60 years or less than 5 years of age, or have comorbidities like diabetes, hypertension or chronic ailments. Avoid meeting visitors at home.</li></div> 	

<div class="row"><li>Washing your hands and face at regular intervals with soap and water.</li></div> 	
	
<div class="row"><li>Wearing a mask at all times when out of the house.</li></div> 	

<div class="row"><li>Social distancing – keeping a distance of 6 feet from others when out of the house.</li></div> 	

<div class="row"><li>Cleaning the frequently touched surfaces with disinfectant regularly.</li></div> 

<div class="row"><li>Avoiding closed spaces with central air-conditioning like offices, malls, etc. with inadequate fresh air</li></div>

<div class="row"><li>Ensuring proper nutrition through balanced diet, maintaining hydration with plenty of fluids, boosting immunity through fresh fruit juices, herbal drinks and turmeric in milk.</li></div> 
	
<div class="row"><li>Daily exercise and meditation.</li></div> 	

<div class="row"><li>Taking your daily prescribed medicines regularly. Don’t self-medicate.</li></div> 	
	
<div class="row"><li>Avoid going near sick people.</li></div>	 

<div class="row"><li>Seek medical advice whenever needed.</li></div>	
	
<div class="row"></ul></div>	

